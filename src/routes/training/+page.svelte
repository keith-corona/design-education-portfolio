        Centralizing Training Materials
      </h2>
      <p class="text-lg">
        During my audit which includes interviews, one of the upper management
        members told me that they really wanted the sales team to be engaging in
        continual education, and they wanted to be able to track progress.
      </p>
      <p class="text-lg">
        The company had about 5 locations that were dispersed within a 100-mile
        radius, so there was not much interaction between sales people at the
        different locations, but they all had a similar environment: slow
        weekdays, very busy weekends. They all sold the same products, so there
        was significant uniformity in what skills and information needed to be
        maintained.
      </p>
      <p class="text-lg">
        My recommendation was to incorporate a learning management system (LMS)
        into their training program. By utilizing an LMS, the company would be
        able to track the material being taught to the sales team, they would be
        able to analyze the progress of the sales people, and by integrating the
        LMS with a sales database, they would be able to track any correlations
        between training and sales. All of this would be able to be done from
        one central location by using the LMS as the central hub.
      </p>
    </div>
  </section>

