        style="line-height:1.1;"
      >
        Don't Overload Students
      </h2>
      <p class="text-lg">
        Although wanting to take full advantage of meeting in person by packing
        the class time with activity upon activity like a conference, students
        can become overwhelmed. It’s important to structure an in person session
        in a hybrid course similar to structuring a traditional in-person
        course.
      </p>
      <p class="text-lg">
        You should begin with some kind of ice breaker and/or warm-up exercise,
        have a clear itinerary presented to the students, and allow enough time
        for each activity. While the aim is no longer set on explaining big
        topics or ideas, there should be an emphasis on eliciting student
        questions primarily through task-based or collaborative activities.
      </p>
      <p class="text-lg">
        Through the completion of this work, students should encounter a
        question they might not have been aware they had prior to beginning the
        activity.
      </p>
    </div>
