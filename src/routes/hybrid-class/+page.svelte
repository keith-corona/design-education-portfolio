      <h2
        class="text-cyan-700 font-semibold text-xl sm:text-3xl md:text-3xl"
        style="line-height:1.1;"
      >
        Embrace Spontaneity
      </h2>
      <p class="text-lg">
        In this hybrid writing course, students were tasked with brainstorming
        ideas related to an essay prompt in small groups during the in-person
        meeting. The essay prompt had been made available to them in the
        asynchronous portion of the course, and they were to read it and note
        any questions.
      </p>
      <p class="text-lg">
        As it often is, there were no questions which sends the teacher the
        message that the material is completely understood, yet when
        collaborating to create a brainstorm, it came to be that students had
        different interpretations of what the prompt was asking for. In their
        small groups, this led to conversation, disagreement, and ultimately, a
        class discussion about the essay prompt.
      </p>
      <p class="text-lg">
        The beauty of the hybrid model is that it supports spontaneity. Students
        can ask a question that can lead to a loosely related discussion. This
        is, more often than not, something that cannot be foreseen by the course
        designer (be it the actual course teacher or an instructional designer),
        and thus is not possible to incorporate in an asynchronous online
        course.
      </p>
      <p class="text-lg">
        Despite this spontaneity being a positive feature of the hybrid model,
        it’s important to not allow these moments to take over the entire
        meeting. The topics brought to light can lead to follow-up readings or
        lessons for the asynchronous part of the course, so leave some room for
        flexibility in the course plan.
      </p>
