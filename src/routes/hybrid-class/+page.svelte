      >
        Consider In-Person Interaction
      </h2>
      <p class="text-lg">
        Compared with fully online or fully in-person courses, developing a
        hybrid course can prove to be extra challenging; the mixed modes of
        teaching add a layer of complication. This added layer, when planned
        well, can prove to be quite beneficial, nevertheless.
      </p>
      <p class="text-lg">
        This hybrid course sample was a writing course, so there needed to be
        interaction in the preliminary stages of the writing process
        (brainstorming, structuring ideas, discussing different paths), while
        the later stages where the writing actually happened would benefit more
        from individual work.
      </p>
      <p class="text-lg">
        How do you accomplish this when the writing process tends to be linear?
        One method is implementing the <a
          class="text-cyan-600 focus:text-amber-600 underline hover:text-cyan-800"
          href="https://bokcenter.harvard.edu/flipped-classrooms"
          target="_blank">flipped classroom</a
        >, and this method, to an extent, was used in this hybrid course.
        Students would access instructional materials in the online portion of
        the course while completing specific tasks during class time.
      </p>
      <p class="text-lg">
        In this class, collaboration was emphasized by assigning a significant
        amount of small-group work to take advantage of the in-person meeting
        time.
      </p>
